// Compression ratio rules from contexts.md
function getOctaneRecommendation(compressionRatio: string): { min: number; ideal: number; fuelIds: string[] } {
  const ratio = parseFloat(compressionRatio);
  if (ratio < 9.0) return { min: 88, ideal: 90, fuelIds: ['fb_001', 'fb_008'] };
  if (ratio < 10.0) return { min: 90, ideal: 90, fuelIds: ['fb_001', 'fb_008'] };
  if (ratio < 11.0) return { min: 90, ideal: 92, fuelIds: ['fb_002', 'fb_004', 'fb_006'] };
  if (ratio < 12.0) return { min: 92, ideal: 95, fuelIds: ['fb_003', 'fb_005', 'fb_007'] };
  return { min: 95, ideal: 98, fuelIds: ['fb_003', 'fb_005', 'fb_007'] };
}

function getOilRecommendation(category: string, certification: string): { dailyIds: string[]; touringIds: string[] } {
  if (category === 'matic') {
    return {
      dailyIds: ['ob_006', 'ob_002', 'ob_003'],
      touringIds: ['ob_102', 'ob_103', 'ob_105'],
    };
  }
  // Sport, naked, bebek, trail use JASO MA/MA2
  return {
    dailyIds: ['ob_001', 'ob_002', 'ob_004', 'ob_007'],
    touringIds: ['ob_101', 'ob_102', 'ob_104', 'ob_105'],
  };
}

export function getKnowledgeBase(motorcycle: Motorcycle): KnowledgeBase {
  const octaneRec = getOctaneRecommendation(motorcycle.compression_ratio);
  const oilRec = getOilRecommendation(motorcycle.category, motorcycle.category === 'matic' ? 'JASO MB' : 'JASO MA2');

  return {
    id: `kb_${motorcycle.id}`,
    motorcycle_id: motorcycle.id,
    min_octane: octaneRec.min,
    ideal_octane: octaneRec.ideal,
    fuel_brands: fuelBrands.filter(fb => octaneRec.fuelIds.includes(fb.id)),
    oil_daily: oilBrands.filter(ob => oilRec.dailyIds.includes(ob.id)),
    oil_touring: oilBrands.filter(ob => oilRec.touringIds.includes(ob.id)),
  };
}
